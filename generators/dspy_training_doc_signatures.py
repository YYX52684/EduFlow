"""
能力训练文档生成：DSPy 签名定义。

输出为单一 Markdown 字符串，章节标题与顺序由 config.TRAINING_DOC_CONFIG 约束。
"""

import dspy

_DOC_OUTPUT_RULES = (
    "输出一篇完整的标准能力训练文档，严格使用 Markdown。"
    "第一行必须是 `# 标题`，标题需包含课程名称与训练主题。"
    "正文必须按顺序包含以下二级标题（`## …`，标题文字一字不差）："
    "## 任务目标、## 智能体人设、## 任务描述、## 智能体各板块任务要求、## 评价标准。"
    "【任务目标】约150字，写清本次能力训练要达成的可观察目标。"
    "【智能体人设】写成具体人物：姓名/称谓+性格+沟通风格+专业背景，贴合学科与材料。"
    "【任务描述】说明智能体如何分板块引导学员完成实训，交互可含指令引导、学员作答、追问、纠错、确认，不限于问答。"
    "【智能体各板块任务要求】按教学材料中的步骤拆分多个实训板块；每个板块用 `### 板块名称` 开头，"
    "下含：场景设定（须结合所给图片中的可观察元素，如工位、部件、工具、界面状态）、"
    "智能体职责与交互流程、学员需要配合的动作；"
    "在该二级标题下必须另设子节 `### 各板块互动话术示例`，"
    "用 Markdown 表格列出至少与板块数量一致的示例行，列至少包含：实训板块、互动环节、智能体话术、学员作答示例；"
    "智能体话术须出现「所给图片」表述，不得出现「ppt」「PPT」「幻灯片」等字样。"
    "【评价标准】总分必须严格为100分：用 Markdown 表格列出评分项、评分描述、对应分值、扣分标准（或说明）；"
    "文中须明确写出总分100分及达标线（如80分及以上）；各「对应分值」列中的分数之和必须等于100。"
    "全文专业严谨、层级清晰；除上述结构外不要添加与训练无关的免责声明；不要输出任何代码块包裹整篇文档。"
)


class TrainingDocSignature(dspy.Signature):
    """根据教师材料生成标准能力训练文档（Markdown）。"""

    course_name: str = dspy.InputField(desc="课程名称，用于文档标题与语境。")
    training_theme: str = dspy.InputField(desc="本次能力训练主题，如某设备维保实训。")
    source_material: str = dspy.InputField(
        desc="教师提供的教学材料全文：讲义、操作规程、分步说明等，须据此拆分板块与要点，不得凭空编造与材料冲突的流程。"
    )
    image_context: str = dspy.InputField(
        desc="所给图片的文字说明：各张图片对应的画面内容、标注、步骤编号等；"
        "若暂无图片说明可填「（暂无）」，则场景设定仍须写「结合所给图片」类引导，并预留与插图配合的表述空间。"
    )

    document_markdown: str = dspy.OutputField(desc=_DOC_OUTPUT_RULES)
